/**
 * Says hello by using a method.
 */
public class SayHelloApp {

    public static void main(String[] args) {
        sayHello(); // invoke sayHello()
    }

    /**
     * Says Hello.
     */
    public static void sayHello() {
        System.out.println("Hello!");
    }



}
